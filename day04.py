# Declaration of variables and input
a = int(input("Enter first number: "))
b = int(input("Enter second number: "))
# Addiction
sum = a + b
print(f"The sum of {a} and {b} is {sum}")

# Substraction
subtraction = a - b
print(f"The subtraction of {a} and {b} is {subtraction}")

# Multiplication
multiplication = a * b
print(f"The multiplication of {a} and {b} is {multiplication}")

# Division
division = a / b
print(f"The division of {a} and {b} is {division}")

# Modulus
modulus = a % b
print(f"The modulus of {a} and {b} is {modulus}")

# Floor Division
floor_division = a // b
print(f"The floor division of {a} and {b} is {floor_division}")

# Exponentiation
exponentiation = a ** b
print(f"The exponentiation of {a} and {b} is {exponentiation}")

